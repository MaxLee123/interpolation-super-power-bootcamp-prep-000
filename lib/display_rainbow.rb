colours = ["red", "orange", "yellow", "green", "blue", "indigo", "violet"]

def display_rainbow(colour)
  puts "R: #{colours[1]}"
  puts "O: #{colours[1]}"
  puts "Y: #{colours[2]}"
  puts "G: #{colours[3]}"
  puts "B: #{colours[4]}"
  puts "I: #{colours[5]}"
  puts "V: #{colours[6]}"
end

display_rainbow(colours)